function playAudio(audio, volume = 1, loop = false) {
    audio.currentTime = 0
    audio.volume = volume
    audio.loop = loop
    audio.play()
}

function playRandomServo() {
  const vol = .73;
  const rand = Math.floor(Math.random() * 3) + 1;

  switch(rand) {
    case 1:
      playAudio(servo1Sound, vol, false);
      return;
    case 2:
      playAudio(servo2Sound, vol, false);
      return;
    default:
      playAudio(servo3Sound, vol, false);
  }
}

